# 딕셔너리(dictionary)
# "이름" = "홍길동", "생일" = "몇 월 며칠" 등과 같은 형식의 자료형. Key와 Value를 한 쌍을 가짐


# 딕셔너리의 형식
# {Key1: Value1, Key2: Value2, Key3: Value3, ...}
# Key: Value 한 쌍으로 값이 이루어져 있고, 쉼표로 구분됨


# 딕셔너리의 에

a = {'name': 'pey', 'phone': '010-9999-1234', 'birth': '1118'}
a = {1: 'hi'} # Key를 정수값으로, Value를 str으로 사용 가능
a = {'a': [1, 2, 3]} # 리스트를 Value값으로 사용 가능